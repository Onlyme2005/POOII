        public void llenarTabla(){
        DefaultTableModel tablaModelo= new DefaultTableModel(new String[] {"nombre","id","Descripcion"},departamentos.size());
        this.tblDepto.setModel(tablaModelo);
        TableModel datosModelo=tblDepto.getModel();
        for (int i = 0; i < departamentos.size(); i++) {
            Departamento d=this.departamentos.get(i);
            datosModelo.setValueAt(d.getNombreDepto(),i,0);
            datosModelo.setValueAt(d.getIdDepto(),i,1);  
            datosModelo.setValueAt(d.getDescripcion(),i,2);

        }
 }

