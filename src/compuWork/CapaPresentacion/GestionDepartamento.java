    private void btnEliminarDeptoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarDeptoActionPerformed
        int fila=this.tblDepto.getSelectedRow();
        if(fila>=0){
            Departamento d=this.departamentos.get(fila);
          int opc=JOptionPane.showConfirmDialog(this,"Deseas eliminar el Departamento: "+d.getNombreDepto());
            if (opc==0) {
            this.gestion.eliminarDepartamento(d.getIdDepto());
            this.departamentos=this.gestion.obtenerDepartamento();
            this.llenarTabla();
            }
        }else{
            JOptionPane.showMessageDialog(this,"Debe seleccionar un departamento a eliminar");
        }
    }//GEN-LAST:event_btnEliminarDeptoActionPerformed

